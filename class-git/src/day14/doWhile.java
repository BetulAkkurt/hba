public class doWhile {public static void main(String[] args) {
    int i = 1;
    while (i <= 5) {
        System.out.println("while hi");
        i++;
    }

    //do-while loop
    int j = 1;
    do {
        System.out.println("do-while Hi");
        j++;
    } while (j <= 5);

    System.out.println("do-while Hi 2");
}
}

