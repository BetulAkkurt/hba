package day15;

public class XEx {
    public static void main(String[] args) {
        System.out.println("for==========");
        for(int i=1; i<=5;i++) {


            System.out.println(i);
        }
        System.out.println("while===========");
        int j=1;
        while(j<=5){
            System.out.println(j);
            j++;
        }
        System.out.println("do-while");
        int m= 1;
        do{
            System.out.println(m);
            m++;

        }while(m<=5);
    }
}
